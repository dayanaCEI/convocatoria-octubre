const dia = 5;
// operador comparacion ===
switch (dia) {
    case 1:
        console.log("lunes");
        break;
    case 2: console.log("martes");
        break;
    case 3: console.log("miercoles");
        break;
    case 4: console.log("jueves");
        break;
    case 5: console.log("viernes");
        break;
    default: console.log("debe ser un numero del 1 al 5");
}